bool range(int left, int right, int target){
  if(left == right)return false;
  long long  mid = left + (right-left)/2;
  if(mid*mid == target){
    return true;
  }
  else if (mid*mid > target){
    return range(left,mid-1,target);
  }
  else{
    return range(mid+1,right,target);
  }
  
  
}
bool isPerfectSquare(int num){
  return range(1,INT_MAX,num);
}
